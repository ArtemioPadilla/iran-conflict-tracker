// ── Projection: lon 25E-65E -> x 0-1000, lat 20N-42N -> y 600-0 ──
export function geoToSVG(lon: number, lat: number): [number, number] {
  return [(lon - 25) / 40 * 1000, (42 - lat) / 22 * 600];
}

// ── Sparkline generator for economic data SVGs ──
export function generateSparkline(data: number[], w: number, h: number): string {
  const min = Math.min(...data) * 0.98;
  const max = Math.max(...data) * 1.02;
  const range = max - min || 1;
  return data
    .map((v, i) => {
      const x = (i / (data.length - 1)) * w;
      const y = h - ((v - min) / range) * h;
      return `${x.toFixed(1)},${y.toFixed(1)}`;
    })
    .join(' ');
}

